let grade = Number(prompt("Please enter the student score: "));

if(grade >= 90){
    alert("The student's grade is A.");
}
else if(grade >= 80){
    alert("The student's grade is B.");
}
else if(grade >= 70){
    alert("The student's grade is C.");
}
else if(grade >= 60){
    alert("The student's grade is D.");
}
else{
    alert("The student's grade is F.");
}



